// Swift Variables
// Variables in Swift are declared using the `var` keyword. Unlike constants, their values can be changed after initialization.
// They are useful for storing data that may vary during the program's execution.

// Example:
var temperature = 70
print("The temperature on Monday morning is \(temperature)°F")
temperature = 80
print("The temperature on Monday evening is \(temperature)°F")

// Key Points:
// - Variables can be reassigned new values after their initial declaration.
// - They are ideal for values that are expected to change, such as user input or dynamic data.
